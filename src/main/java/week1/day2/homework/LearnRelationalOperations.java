package week1.day2.homework;

public class LearnRelationalOperations {

	public static void main(String[] args) {
	
		int no1=10,no2=20;
		int no3=14,no4=14;
		System.out.println("Equals to : " + (no1==no2));
		System.out.println("NOt equal to : "+(no1!=no2));
		System.out.println("Less than : "+(no1<no2));
		System.out.println("Less than or equal to : "+(no3<=no4));
		System.out.println("Greater than : "+(no1>no2));
		System.out.println("Greater than : "+(no3>=no4));
	}

}
