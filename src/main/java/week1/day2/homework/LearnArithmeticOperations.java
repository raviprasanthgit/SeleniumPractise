package week1.day2.homework;

public class LearnArithmeticOperations {

	public static void main(String[] args) {
	
		int no1=10,no2=20;
		
		System.out.println("Addition : " +(no1+no2));
		System.out.println("Subtraction : "+(no2-no1));
		System.out.println("Division : "+(no2/no1));
		System.out.println("Remainder : "+(no2/no1));
		System.out.println("Multiplication : "+(no1*no2));
	}

}
