package week1.day2.homework;

public class LearnIncrementAndDecrementOperations {

	public static void main(String[] args) {
	
		int no1=10,no2=20;
		int no3=14,no4=14;
		System.out.println("post increment :" + (no1++));
		System.out.println("Pre incremnet : "+(++no2));
		System.out.println("post decrement :" + (no3--));
		System.out.println("Pre decrement : "+(--no4));
	}

}
